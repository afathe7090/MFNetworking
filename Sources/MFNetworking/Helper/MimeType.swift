import Foundation

public enum MimeType: String {
    case jpeg = "image/jpeg"
    case png = "image/png"
    case gif = "image/gif"
    case bmp = "image/bmp"
    case webp = "image/webp"
    case tiff = "image/tiff"
    case mp3 = "audio/mpeg"
    case wav = "audio/wav"
    case ogg = "audio/ogg"
    case mp4 = "video/mp4"
    case m4a = "audio/mp4"
    case mov = "video/quicktime"
    case avi = "video/x-msvideo"
}
